    private void salvar() {
        Pessoa p = new Pessoa();
        p.setCpf(tfCpf.getText());
        p.setData_nascimeto(tfNascimento.getText());
        p.setEmail(tfEmail.getText());
        p.setIdentidade(tfIdentidade.getText());
        p.setNome(tfNome.getText());

        PessoaControl control = new PessoaControl();
        if (control.insert(p)) {
            JOptionPane.showMessageDialog(rootPane, "Cadastro realizado com sucesso!", "Informação", JOptionPane.INFORMATION_MESSAGE);
            this.dispose();
            cleanFields();
        }

    }

    private void cleanFields() {
        tfCpf.setText("");
        tfEmail.setText("");
        tfIdentidade.setText("");
        tfNascimento.setText("");
        tfNome.setText("");
    }


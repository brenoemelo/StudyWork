    private void openNovoItem() {
        viewAdicionaItemSimples ad = new viewAdicionaItemSimples(null, true);
        if (ad.setIndisponivel(pedido.getItems())) {
            ad.setVisible(true);

            if (ad.getRetorno() != null) {
                pedido.getItems().addAll(ad.getRetorno());
                refreshTable();
            }
        }
    }

    private void refreshTable() {
        tableItensPedido1.addData(pedido.getItems());
        lbTotal.setText(Formats.BR_CURRENCY.format(pedido.getTotalPedido()));
    }

    private void remover() {
        if (tableItensPedido1.getSelectedRowCount() > 0) {
            pedido.getItems().removeAll(tableItensPedido1.getSelecionados());
            refreshTable();
        }
    }

    private void fechaPedido() {
        if (validaPedido()) {
            PedidoControl control = new PedidoControl();
            pedido.setCliente(selecionaPessoaTextField1.getSelecionado());
            pedido.setConcluido(true);
            pedido.setData(new Date());
            pedido.setFilial(livrariaComboBox1.getItemAt(livrariaComboBox1.getSelectedIndex()));
            pedido.setFinalizado(true);
            pedido.setUsuario(SystemLogin.USUARIO_LOGADO);
            if (control.insert(pedido)) {
                JOptionPane.showMessageDialog(null, "Pedido inserido com sucesso!", "Atenção", JOptionPane.INFORMATION_MESSAGE);
                this.limparTela();
                this.setVisible(false);
            }
        }
    }

    private void limparTela() {
        pedido = new Pedido();
        refreshTable();
    }

    private boolean validaPedido() {
        if (selecionaPessoaTextField1.getSelecionado() == null) {
            JOptionPane.showMessageDialog(null, "Selecione um cliente para continuar.", "Atenção", JOptionPane.INFORMATION_MESSAGE);
            return false;
        }
        if (pedido.getItems().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Adicione itens ao pedido para continuar.", "Atenção", JOptionPane.INFORMATION_MESSAGE);
            return false;
        }
        return true;
    }


    private boolean valida() {
        if (tfNome.getText().replace(" ", "").isEmpty()) {
            JOptionPane.showMessageDialog(null, "Informe um nome válido!", "Atenção", JOptionPane.INFORMATION_MESSAGE);
            tfNome.requestFocus();
            return false;
        }

        return true;
    }


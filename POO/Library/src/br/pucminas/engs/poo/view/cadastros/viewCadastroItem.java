    private void salvarItem(Item l) {
        try {
            l.setAno(Integer.parseInt(tfAno.getText()));
            l.setEdicao(Integer.parseInt(tfEdicao.getText()));
            l.setEditora_item((Editora) editoraComboBox1.getSelectedItem());
            l.setNome(tfNome.getText());

            l.setPeso(Integer.parseInt(tfPeso.getText()));
            l.setPreco(Double.parseDouble(tfPreco.getText()));
            l.setTamanhoL(Double.parseDouble(tfLargura.getText()));
            l.setTamanhoA(Double.parseDouble(tfAltura.getText()));
            l.setNumero_paginas(Integer.getInteger(tfPaginas.getText()));
            l.setTipo_item(TipoItem.LIVRO);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }

    private void salvarLivro() {
        if (valida()) {
            try {
                LivroControl cLivro = new LivroControl();
                Livro l = new Livro();
                salvarItem(l);
                l.setTipo_capa(selecionaTipoCapaComboBox1.getSelecionado());
                l.setIsbn(tfIsbn.getText());
                if (cLivro.insert(l)) {
                    JOptionPane.showMessageDialog(null, "Livro cadastrado com sucesso!");
                    limparCampos();
                    this.setVisible(false);
                } else {
                    JOptionPane.showMessageDialog(null, "Livro cadastrado com sucesso!");
                }

            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(null, e.toString(), "Erro de Conversão", JOptionPane.INFORMATION_MESSAGE);
            }
        }
    }


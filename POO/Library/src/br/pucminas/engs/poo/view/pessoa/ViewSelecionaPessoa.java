    private void search() {
        Pessoa p = new Pessoa();
        boolean search = false;
        if (!tfNome.getText().isEmpty()) {
            p.setNome(tfNome.getText());
            search = true;
            System.out.println("search nome");
        }

        if (tfCPF.getText().replace(" ", "").length() == 14) {
            p.setCpf(tfCPF.getText());
            search = true;
            System.out.println("search cpf");
        }
        if (!tfEmail.getText().isEmpty()) {
            p.setEmail(tfEmail.getText());
            search = true;
        }

        if (search) {
            tablePessoas1.doSearch(p);
        } else {
            tablePessoas1.doSearch(null);
        }
    }

    private void seleciona() {
        if (tablePessoas1.getSelecionado() != null) {
            retorno = tablePessoas1.getSelecionado();
            this.setVisible(false);
        }
    }

